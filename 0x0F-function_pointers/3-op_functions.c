/**
 * op_add - add up to numbers and return result
 * @a: first number
 * @b: seconnd number
 *
 * Return: sum of the two numbers
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtract b from a and return difference
 * @a: first number
 * @b: seconnd number
 *
 * Return: difference of two numbers
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiply a and b and return result
 * @a: first number
 * @b: seconnd number
 *
 * Return: product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide a by b and return quotient
 * @a: first number
 * @b: seconnd number
 *
 * Return: quotient of the two numbers
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - return the remainder of a / b
 * @a: first number
 * @b: seconnd number
 *
 * Return: mod a / b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
